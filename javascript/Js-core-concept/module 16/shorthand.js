// variable declaraition
var tomattoPrice = 100;
// update variable value
tomattoPrice = 150;
// add something in variable
tomattoPrice = tomattoPrice + 100;
// shorthand
// += means add value with old variable
tomattoPrice += 80;
// -= means substract value from old variable
tomattoPrice -= 20;
// ++ means add 1 with variable
tomattoPrice++;
// -- means substract 1 from old variable
tomattoPrice--;
console.log(tomattoPrice);
