var number = [10, 20, 30];
// add new item in array at last position
number.push(40);
number.push(50);
console.log(number);
// add new item in array at first position
number.unshift(11);
console.log(number);
// remove last item in array
number.pop();
number.pop();
console.log(number);
// remove first item in array
number.shift();
console.log(number);
